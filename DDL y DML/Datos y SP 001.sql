		SELECT ab.scooter AS SCOOTER,ab.fecha_abastecer AS 'FECHA',ab.costo_abastecer AS 'COSTO' 
			FROM abastecer As ab 
			WHERE scooter LIKE CONCAT("%",sco,"%");
	ELSE
		SELECT ab.scooter AS SCOOTER,ab.fecha_abastecer AS 'FECHA',ab.costo_abastecer AS 'COSTO' 
			FROM abastecer As ab 
			WHERE ab.scooter LIKE CONCAT("%",sco,"%") AND ab.fecha_abastecer = fch;
	END IF;
end ;;
